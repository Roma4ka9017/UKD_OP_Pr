week_day = input("Введіть день тижня: ").lower()

if week_day == "понеділок":
    print("Понеділок - день-нероба")
elif week_day == "вівторок":
    print("Вівторок - повторник")
elif week_day == "середа":
    print("Середа - тамада")
elif week_day == "четвер":
    print("Четвер - всі турботи я відкинув")
elif week_day == "пʼятниця":
    print("П'ятниця - п'яниця")
elif week_day == "субота":
    print("Субота – не робота")
elif week_day == "неділя":
    print("Неділя – день веселощів.")